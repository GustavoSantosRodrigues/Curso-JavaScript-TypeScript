const nome = prompt('Digite seu nome Completo: ');
document.body.innerHTML += `O seu nome é: ${nome} <br/>`;
document.body.innerHTML += `Seu nome tem: ${nome.length} letras <br/>`;
document.body.innerHTML += `A segunda letra do seu nome é: ${nome[1]} <br/>`;
document.body.innerHTML += `Qual o primeiro indice da T no seu nome: ${nome.indexOf('t')} <br/>`;
document.body.innerHTML += `Qual ultimo indice do seu nome: ${nome.lastIndexOf('s')} <br/>`;
document.body.innerHTML += `As ultimas 3 letras do seu nome sao: ${nome.slice(-3)} <br/>`; 
document.body.innerHTML += `As palavras do seu nome são: ${nome.split(' ')} <br/>`;
document.body.innerHTML += `Seu nome em letra maiscula: ${nome.toUpperCase()} <br/>`;  
document.body.innerHTML += `Seu nome em letra minuscula: ${nome.toLowerCase()} <br/>`; 